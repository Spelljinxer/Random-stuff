import java.util.*;

public class EuclidAlgorithm
{
    public int euclid(int a, int b)
    {
        while(a != b)
        {
            if(a > b)
            {
                a = a - b;
            }
            else
            {
                b = b - a;
            }
        }
        return a;
    }

    public static void main(String [] args)
    {
        int a = 0; // <--- insert a
        int b = 0; // <--- insert b
        EuclidAlgorithm obj = new EuclidAlgorithm();
        System.out.println(obj.euclid(a, b));
    }
}